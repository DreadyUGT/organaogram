    <ol>
        <li>Veranderkalender Unigarant: De functioneel ontwerper analyseert hierbij de wens en vertaalt deze, in afstemming met de opdrachtgever, naar een oplossing die past in de inrichting van Pulse.</li>
    </ol>
   
