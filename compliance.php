    	De Compliance Officer ondersteunt het bestuur en het management bij het voldoen aan de wet- en regelgeving. De Compliance Officer adviseert en begeleidt. De verantwoordelijkheid van het compliant zijn van de organisatie ligt bij het bestuur en het management. Daarnaast monitort de Compliance Officer of de wetgeving goed in de onderneming is geïmplementeerd. <br/><br/>

    	Een andere taak is het bevorderen van een werkomgeving waarin 'integer' werken voorop staat. Integriteit hoort tussen de oren te zitten, maar er is natuurlijk ook het nodige geschreven, bijvoorbeeld in onze gedragscodes. Hierbij moet je denken aan gedragscodes met betrekking tot bijvoorbeeld klokkenluiden, relatiegeschenken en nevenfuncties. Deze raken iedere medewerker van UVM/Unigarant. Daarom is het belangrijk om over Compliance te praten en uit te leggen wat er precies in de codes staat.
